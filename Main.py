a = int(input("Enter the Number: ")) # Taking the Input from the User.
b = a ** (1.0/3.0) # Finding its Cube Root. Remember that two '**' mean raise to the Power.
c = round(b) # This line Rounds off the Output to Provide us with the Correct Answer.

print(f"The Cube Root of {a} is {c}.") # Printing the Results.